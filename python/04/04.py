# Operadores de comparacion
print(2 == 2)
print(2 == 1)

# Al comparar strings las mayusculas o tipos cuentan
print("2" == 2)

# Al comparar float con int si son el mismo dara True
print(3.0 == 3)

# Combinacion de operadores logicos
print(1 < 2 > 3)

# Con and todos deben ser verdaderos para que el resultado sea verdadero
print(1 < 2 and 2 > 3)

# Con or tan solo hace falta que una de las expresiones sea verdadera
print(1 < 2 or 2 > 3)

# not devuelve lo contrario del resultado de una expresion, es lo que en
# javascript se expresa con la exclamacion inicial
print(not(1 == 1))
